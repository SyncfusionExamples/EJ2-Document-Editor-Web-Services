package ej2.webservices;

public class CustomParameter {
    public String content;
    public String type;
    
    public String getContent() {
		return content;
	}

	public String getType() {
		return type;
	}
	
	public void setContent(String value) {
		content= value;
	}
	
	public void setType(String value) {
		type = value;
	}
}
